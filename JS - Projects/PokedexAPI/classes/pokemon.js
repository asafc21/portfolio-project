class Pokemon {
  constructor(id, img, name, type, hp, atk, def, spAtk, spDef, speed) {
    this.id = id;
    this.img = img;
    this.name = name;
    this.type = type;
    this.hp = hp;
    this.atk = atk;
    this.def = def;
    this.spAtk = spAtk;
    this.spDef = spDef;
    this.speed = speed;
  }
}

export { Pokemon };
